class Object3D {

	constructor(program = defaultProgram, ka = [0.5, 0.5, 0.5, 1.0], kd = [0.5, 0.5, 0.5, 1.0], ks = [0.5, 0.5, 0.5, 1.0]) {

		this.posVBO = gl.createBuffer();
		this.indexVBO = gl.createBuffer();

		this.positions = [];
		this.indices = [];

		this.ka = ka;
		this.kd = kd;
		this.ks = ks;
		this.specularExponent = 4.0;

		this.position = [0, 0, 0];
		this.orientation = [0, 0, 0];
		this.scale = [1, 1, 1];
		this.modelMatrix;

		this.shader = program;
		gl.useProgram(this.shader);

		this.posLoc = gl.getAttribLocation(this.shader, "vPosition");
		this.normalLoc = gl.getAttribLocation(this.shader, "vNormal");
		this.modelMatrixLoc = gl.getUniformLocation(this.shader, "modelMatrix");
		this.viewMatrixLoc = gl.getUniformLocation(this.shader, "viewMatrix");
		this.projectionMatrixLoc = gl.getUniformLocation(this.shader, "projectionMatrix");
		this.lightPositionLoc = gl.getUniformLocation(this.shader, "lightPosition");
		this.IaLoc = gl.getUniformLocation(this.shader, "Ia");
		this.IdLoc = gl.getUniformLocation(this.shader, "Id");
		this.IsLoc = gl.getUniformLocation(this.shader, "Is");
		this.kaLoc = gl.getUniformLocation(this.shader, "ka");
		this.kdLoc = gl.getUniformLocation(this.shader, "kd");
		this.ksLoc = gl.getUniformLocation(this.shader, "ks");
		this.specularExponentLoc = gl.getUniformLocation(this.shader, "specExp");

		this.SetModelMatrix();
	}

	InitBuffers() {

		gl.bindBuffer(gl.ARRAY_BUFFER, this.posVBO);
		gl.bufferData(gl.ARRAY_BUFFER, new Float32Array(this.positions), gl.STATIC_DRAW);
		
		gl.bindBuffer(gl.ELEMENT_ARRAY_BUFFER, this.indexVBO);
		gl.bufferData(gl.ELEMENT_ARRAY_BUFFER, new Uint16Array(this.indices), gl.STATIC_DRAW);
	}

	SetModelMatrix (position = this.position, orientation = this.orientation, scale = this.scale) {
		
		this.position = position;
		this.orientation = [orientation[0] * Math.PI / 180, orientation[1] * Math.PI / 180, orientation[2] * Math.PI / 180]; // Convert the orientation to RAD
		this.scale = scale;
		
		this.modelMatrix = mat4.create();
		mat4.translate(this.modelMatrix, this.modelMatrix, position);
		mat4.rotate(this.modelMatrix, this.modelMatrix, this.orientation[0], [1, 0, 0]);
		mat4.rotate(this.modelMatrix, this.modelMatrix, this.orientation[1], [0, 1, 0]);
		mat4.rotate(this.modelMatrix, this.modelMatrix, this.orientation[2], [0, 0, 1]);
		mat4.scale(this.modelMatrix, this.modelMatrix, scale);
	}

	UpdateUniforms () {

		gl.useProgram(this.shader);

		gl.uniformMatrix4fv(this.modelMatrixLoc, false, this.modelMatrix);

		gl.uniform4fv(this.kaLoc, this.ka);
		gl.uniform4fv(this.kdLoc, this.kd);
		gl.uniform4fv(this.ksLoc, this.ks);
		gl.uniform1f(this.specularExponentLoc, this.specularExponent);
	}

	Render() {

		gl.useProgram(this.shader);

		// Link data in VBO to shader variables
		gl.bindBuffer(gl.ARRAY_BUFFER, this.posVBO);
		gl.enableVertexAttribArray(this.posLoc);
		gl.enableVertexAttribArray(this.normalLoc);
		
		gl.vertexAttribPointer(this.posLoc, 3, gl.FLOAT, false, 2 * 3 * 4, 0);
		gl.vertexAttribPointer(this.normalLoc, 3, gl.FLOAT, false, 2 * 3 * 4, 3 * 4);

		this.UpdateUniforms();

		// Render
		gl.bindBuffer(gl.ELEMENT_ARRAY_BUFFER, this.indexVBO);
		gl.drawElements(gl.TRIANGLES, this.indices.length, gl.UNSIGNED_SHORT, 0);
	}
}


